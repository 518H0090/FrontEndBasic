// Type conversion
// change the data type of value to another (string => boolean, number,float,byte)

// string only
var valueString = "20";
valueString += 1;
console.log(valueString);

// convert string to number
var age = Number(20);
age += 1;
console.log(age);

// convert number to String
var x = 20;
valueX = String(x);
console.log(valueX);

// convert string to boolean
let isTrueString = "True";
console.log(isTrueString);
let isTrue = Boolean(isTrueString);

console.log(isTrue);
